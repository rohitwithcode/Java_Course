package Chapter_2_Operators_and_Expressions;

public class Quick_Quiz {
    public static void main(String[] args) {
        int x=3;
        int y=4;
        System.out.println("x-y/2="+(x-y)/2);

        int b=3;
        int a=5;
        float c=2;
        System.out.println("b^2-4ac/2a="+((b*b -(4*a*c))/(2*a)));

        int v=5;
        int u=3;
        System.out.println("v^v-u^u="+(v*v - u*u));

        int d=4;
        System.out.println("a*b-d="+(a*b-d));
    }
}
